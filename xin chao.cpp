#include<stdio.h>

int main(){
	// printf : in ra man hin
	printf("xin chao\n");
	printf("ban bao nhieu tuoi\n");
	printf("toi 18 tuoi\n");
	printf("ban chao bao nhieu\n");
	printf("toi cao 1.79 m\n");
	// bien (variable) : nhung dua con
	// co nhung cai nhiem vu cu the
	// phai dat ten de co the goi bat ky luc nao
	// cu phap : <kieu du lieu> <ten bien>;
	int tuoi;
	//gan gia tri 
	tuoi = 18;
	printf("ban bao nhieu tuoi\n");
	printf("toi %d tuoi\n", tuoi);
	double chieucao = 1.79;
	printf("ban cao bao nhieu\n");
	printf("toi cao %lf m\n", chieucao);
	// kieu du lieu + cach goi
	// int            %d  (digit)
	// float          %f  (float) //khong nen dung
	// double         %lf (large float)
}
